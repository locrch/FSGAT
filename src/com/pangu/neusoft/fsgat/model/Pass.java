package com.pangu.neusoft.fsgat.model;

public class Pass
{
	private String passNumber,surName,givenName,dob,sex,pob,expireDate,issueDate,issueArea,area;

	public String getPassNumber()
	{
		return passNumber;
	}

	public void setPassNumber(String passNumber)
	{
		this.passNumber = passNumber;
	}

	public String getSurName()
	{
		return surName;
	}

	public void setSurName(String surName)
	{
		this.surName = surName;
	}

	public String getGivenName()
	{
		return givenName;
	}

	public void setGivenName(String givenName)
	{
		this.givenName = givenName;
	}

	public String getDob()
	{
		return dob;
	}

	public void setDob(String dob)
	{
		this.dob = dob;
	}

	public String getSex()
	{
		return sex;
	}

	public void setSex(String sex)
	{
		this.sex = sex;
	}

	public String getPob()
	{
		return pob;
	}

	public void setPob(String pob)
	{
		this.pob = pob;
	}

	public String getExpireDate()
	{
		return expireDate;
	}

	public void setExpireDate(String expireDate)
	{
		this.expireDate = expireDate;
	}

	public String getIssueDate()
	{
		return issueDate;
	}

	public void setIssueDate(String issueDate)
	{
		this.issueDate = issueDate;
	}

	public String getIssueArea()
	{
		return issueArea;
	}

	public void setIssueArea(String issueArea)
	{
		this.issueArea = issueArea;
	}

	public String getArea()
	{
		return area;
	}

	public void setArea(String area)
	{
		this.area = area;
	}
}
